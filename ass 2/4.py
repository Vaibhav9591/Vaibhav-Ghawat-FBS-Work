# 4 WAP to calculate area of triangle and rectangle

height= int(input("enter a height of triangle: "))
base= int(input("enter a base of triangle: "))

area_of_triangle = 0.5*base*height

lenght= int(input("enter a lenght of rectangle: "))
breadth= int(input("enter a breadth of rectangle: "))

area_of_rectangle = lenght*breadth

print(f'area of triangle is: {area_of_triangle}\narea of rectangle is: {area_of_rectangle}')
